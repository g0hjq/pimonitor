// Get the current CPU usage from /proc/stat
CpuStats get_cpu_stats(void) {
    CpuStats stats = {0};
    FILE *fp = fopen("/proc/stat", "r");
    if (fp == NULL) {
        perror("Error opening /proc/stat");
        exit(1);
    }

    char buffer[256];
    fgets(buffer, sizeof(buffer), fp);
    sscanf(buffer, "cpu %llu %llu %llu %llu %llu %llu %llu",
           &stats.user, &stats.nice, &stats.system, &stats.idle,
           &stats.iowait, &stats.irq, &stats.softirq);
    fclose(fp);
    return stats;
}

// Calculate the CPU percent over the previous second
float calculate_cpu_usage(CpuStats prev, CpuStats curr) {
    unsigned long long prev_total = prev.user + prev.nice + prev.system +
                                    prev.idle + prev.iowait + prev.irq + prev.softirq;
    unsigned long long curr_total = curr.user + curr.nice + curr.system +
                                    curr.idle + curr.iowait + curr.irq + curr.softirq;
    unsigned long long total_diff = curr_total - prev_total;
    unsigned long long idle_diff = curr.idle - prev.idle;
    if (total_diff == 0) return 0.0;
    return 100.0 * (total_diff - idle_diff) / total_diff;
}




