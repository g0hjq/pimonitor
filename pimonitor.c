// Read the Raspberry Pi CPU temperature in degrees Celsius
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------


float get_rpi_temperature(void)
{
    FILE *fp;

    // Open the temperature file
    fp = fopen("/sys/class/thermal/thermal_zone0/temp", "r");
    if (fp == NULL)
    {
        perror("/sys/class/thermal/thermal_zone0/temp");
        exit(1);
    }

    // Read the temperature (in millidegrees Celsius)
    int temp_milli = -999000;
    fscanf(fp, "%d", &temp_milli);
    fclose(fp);

    return temp_milli / 1000.0;
}



