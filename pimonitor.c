// Send a single command to the SSD1306
void write_command(uint8_t cmd) {
    uint8_t buf[2] = {0x00, cmd}; // Control byte 0x00 for commands
    if (write(i2c_fd, buf, 2) != 2) {
        perror("Failed to write command");
    }
}


// Send a block of data to the SSD1306
void write_data(uint8_t *data, size_t len) {
    uint8_t tmp_buff[1+sizeof(buffer)];

    if (len+1 > sizeof(tmp_buff)) {
        perror("write_data overflow");
        exit(1);
    }

    tmp_buff[0] = 0x40; // Control byte 0x40 for data
    memcpy(tmp_buff+1, data, len);

    if (write(i2c_fd, tmp_buff, len+1) != len+1) {
        perror("Failed to write data");
    }
}


// Write the entire frame buffer contents to the display
void update_display(void) {
    write_command(0x20); write_command(0);  // Set horizontal addressing mode
    write_command(0x21); write_command(0); write_command(WIDTH - 1); // Set column address range
    write_command(0x22); write_command(0); write_command(PAGES - 1); // set page address range

    write_data(buffer, sizeof(buffer)); // send framebuffer data
}

// Initialize the SSD1306
void ssd1306_init(void) {

    // Initialise the I2C connection to the OLED Display
    i2c_fd = open(I2C_BUS, O_RDWR);
    if (i2c_fd < 0) {
        perror("Failed to open I2C bus");
        exit(1);
    }

    if (ioctl(i2c_fd, I2C_SLAVE, OLED_I2C_ADDRESS) < 0) {
        perror("Failed to set I2C address");
        close(i2c_fd);
        exit(1);
    }

    // SSD1306 initialization sequence
    uint8_t init_commands[] = {
        0xAE,       // Display OFF
        0xD5, 0x80, // Set Display Clock Divide Ratio / OSC Frequency
        0xA8, 0x3F, // Set Multiplex Ratio (128x64)
        0xD3, 0x00, // Set Display Offset
        0x40,       // Set Display Start Line
        0x8D, 0x14, // Enable Charge Pump (Internal DC/DC)
        0xA1,       // Set Segment Re-Map
        0xC8,       // Set COM Output Scan Direction
        0xDA, 0x12, // Set COM Hardware Configuration
        0x81, 0xCF, // Set Contrast
        0xD9, 0xF1, // Set Pre-Charge Period (Internal)
        0xDB, 0x40, // Set VCOMH Deselect Level
        0xA4,       // Entire Display ON (not resume)
        0xA6,       // Normal display (not inverted)
        0xAF        // Display ON
    };

    for (size_t i = 0; i < sizeof(init_commands); i++) {
        write_command(init_commands[i]);
    }
	
	memset(buffer, 0, sizeof(buffer));
	update_display();
}


// Turn off the Display and release the I2C connection
void cleanup(void) {
    if (i2c_fd >= 0) {
		clear_buffer();
		update_display();
		
        write_command(0xAE); // Display OFF
        close(i2c_fd);
		i2c_fd = -1;
    }
}


void set_contrast_high(void) {
    write_command(0x81);
    write_command(0xCF);
}


void set_contrast_low(void) {
    write_command(0x81);
    write_command(0x04);
}

