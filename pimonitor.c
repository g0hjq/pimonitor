// Delay for the specified number of milliseconds
void nanosleep_ms(unsigned int ms) {
    struct timespec ts;
    ts.tv_sec = ms / 1000;
    ts.tv_nsec = (ms % 1000) * 1000000;
    nanosleep(&ts, NULL);
}


// Clear the framebuffer
void clear_buffer(void) {
    memset(buffer, 0, sizeof(buffer));
}


// Draw a pixel in the framebuffer
void set_pixel(int x, int y) {
    if (x >= 0 && x < WIDTH && y >= 0 && y < HEIGHT) {
        int page = y / 8;
        int bit = y % 8;
        buffer[page * WIDTH + x] |= (1 << bit);
    }
}


// Obtain the IPv4 network IP address and interface name
void get_ipaddr(char* interface_name, char* ip_address) {

	struct ifaddrs *ifaddr, *ifa;
    int sock;
    struct ifreq ifr;
	
    // Create a socket for ioctl
    sock = socket(AF_INET, SOCK_DGRAM, 0);
    if (sock == -1) {
        perror("socket");
        return;
    }

    // Get list of interfaces
    if (getifaddrs(&ifaddr) == -1) {
        perror("getifaddrs");
        close(sock);
        return;
    }

    // Iterate through interfaces
    for (ifa = ifaddr; ifa != NULL; ifa = ifa->ifa_next) {
		
        // Skip interfaces without an address or non-IPv4
        if (ifa->ifa_addr == NULL || ifa->ifa_addr->sa_family != AF_INET) {
            continue;
        }

		// Skip loopback interface
        if (strcmp(ifa->ifa_name, "lo") == 0) {
            continue;
        }
		
        // Check if the interface is UP using ioctl
        strncpy(ifr.ifr_name, ifa->ifa_name, IFNAMSIZ - 1);
        if (ioctl(sock, SIOCGIFFLAGS, &ifr) == -1) {
            perror("ioctl");
            continue;
        }

        // Get IP address
        if (ifr.ifr_flags & IFF_UP) {
            if (ioctl(sock, SIOCGIFADDR, &ifr) == -1) {
                continue; // Skip if no IP address
            }

            struct sockaddr_in *addr = (struct sockaddr_in *)&ifr.ifr_addr;							
			strcpy(ip_address, inet_ntoa(addr->sin_addr));
			strcpy(interface_name, ifa->ifa_name);
			break;
        }
    }

    // Clean up
    freeifaddrs(ifaddr);
    close(sock);
}


