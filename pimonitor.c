// Returns a pointer to the selected font data.
// (Used by print_text and get_string_width)
FONT_INFO* get_font_info(FONT_SIZE_TYPE font_size)
{
    switch (font_size)
    {
        case FONT_SIZE_SMALL:
            return &calibri_12ptFontInfo;
            break;
        case FONT_SIZE_MEDIUM:
            return &calibri_14ptFontInfo;
            break;
        case FONT_SIZE_LARGE:
            return &calibri_22ptFontInfo;
            break;
        case FONT_SIZE_GIANT:
            return &calibri_28ptFontInfo;
		default:
            return &calibri_12ptFontInfo; // fallback
			break;
    }
}


// Returns the number of pixels needed to print the supplied string
uint32_t get_string_width(char* txt, FONT_SIZE_TYPE font_size)
{
    FONT_INFO* fontinfo_p = get_font_info(font_size);

    // first character in the descriptor table
    uint8_t start_char = fontinfo_p->start_char;

    // Pointer to the start of the descriptor table
    FONT_CHAR_INFO* descriptor_table_p = fontinfo_p->charinfo_p;

    // get the total of all the character widths
    uint32_t width = 0;

    for (const char* ptr = txt; *ptr; ptr++) {
        char ch = *ptr;
        if ((ch >= start_char) && (ch <= fontinfo_p->end_char)) {
            FONT_CHAR_INFO* charinfo_p = &descriptor_table_p[ch-start_char];
            width += charinfo_p->width_bits + fontinfo_p->spacing;
        }
    }

    // Adjust for no spacing at the end
    if (width > fontinfo_p->spacing)
		return width - fontinfo_p->spacing;
	else
		return 0;
}


// Prints a left-justified line of text at the supplied co-ordinates
void print_string(uint32_t start_x,
                  uint32_t start_y,
                  char* txt,
                  FONT_SIZE_TYPE font_size)
{
    // get the font_info
    FONT_INFO* fontinfo_p = get_font_info(font_size);

    // Pointer to the start of the descriptor table
    FONT_CHAR_INFO* descriptor_table_p = fontinfo_p->charinfo_p;

    // Pointer to the start of the bitmap byte array
    uint8_t* bitmap_p = fontinfo_p->bitmap_p;


	for (char* ptr = txt; *ptr; ptr++) {

        char ch = *ptr;

        if ((ch < fontinfo_p->start_char) || (ch > fontinfo_p->end_char))
        {
            printf("pi_oled_status: character out of range : %c\n", ch);
            continue;
        }

        // get the font char info table entry
        FONT_CHAR_INFO* charinfo_p = &descriptor_table_p[ch - fontinfo_p->start_char];
        uint8_t char_width = charinfo_p->width_bits;

        if (char_width)
        {
            uint8_t* data_p = bitmap_p + charinfo_p->offset;

            for (uint32_t y=0; y < fontinfo_p->height; y++)
            {
                uint8_t mask = 0x80;
                for (uint32_t x=0; x<char_width; x++)
                {
                    if ((mask & *data_p) > 0) {
                        set_pixel(start_x+x, start_y+y);
                    }

                    mask = mask >> 1;
					
                    if ((mask == 0) || ((x+1) == char_width)) {
                        data_p++;
                        mask = 0x80;
                    }
                }
            }

            start_x = start_x + char_width + fontinfo_p->spacing;
        }
        else
        {
            printf("pi_oled_status: No character bitmap for %c\n", ch);
        }

    }
}

// prints a centred line of text, horizontally centered on start_x.
void print_string_centered (
                  uint32_t start_x,
                  uint32_t start_y,
                  char* txt,
                  FONT_SIZE_TYPE font_size)
{
    uint32_t width = get_string_width(txt, font_size);
    print_string(start_x - (width/2), start_y, txt, font_size);
}

// prints a right justified line of text, start_x is the right most point.
void print_string_right (
                uint32_t start_x,
                uint32_t start_y,
                char* txt,
                FONT_SIZE_TYPE font_size)
{
    uint32_t width = get_string_width(txt, font_size);
    print_string(start_x - width, start_y, txt, font_size);
}




