// displays hostname and ip address for 10 seconds
void show_splashscreen() {

	char host_name[80];
	char interface_name[80];
	char ip_address[80];
	
    if (gethostname(host_name, sizeof(host_name)) == -1) {
        perror("gethostname");
        return;
    }
	
	get_ipaddr(interface_name, ip_address);

	clear_buffer();	
	print_string_centered(WIDTH/2, 0, host_name, FONT_SIZE_MEDIUM);
	print_string_centered(WIDTH/2, 28, interface_name, FONT_SIZE_MEDIUM);
	
	// Print the IP address smaller if it won't fit on the screen otherwise
	if (get_string_width(ip_address, FONT_SIZE_MEDIUM) >= WIDTH) {
		print_string_centered(WIDTH/2, 48, ip_address,  FONT_SIZE_SMALL);
	}
	else {
		print_string_centered(WIDTH/2, 48, ip_address,  FONT_SIZE_MEDIUM);
	}	

	update_display();
}



// displays time, CPU%, Memory% and Temperature
void show_status(float cpu_usage, float ram_usage, float temp_c) {

    time_t rawtime;
    struct tm *timeinfo;
	char tmp_str[80];
	
    // Get the local time
    time(&rawtime);
    timeinfo = localtime(&rawtime);

	clear_buffer();

	if (timeinfo->tm_sec%15 < 5) {
		strftime(tmp_str, sizeof(tmp_str), "%a %d %b", timeinfo);
	}
	else {
		strftime(tmp_str, sizeof(tmp_str), "%H : %M : %S", timeinfo);
	}
	print_string_centered(WIDTH/2, 0, tmp_str, FONT_SIZE_MEDIUM);	
	
	print_string(4, 20, "CPU", FONT_SIZE_MEDIUM);
	sprintf(tmp_str, "%.0f %%", cpu_usage);
	print_string_right(WIDTH-4, 20, tmp_str, FONT_SIZE_MEDIUM);
	
	print_string(4, 35, "Temp", FONT_SIZE_MEDIUM);
	sprintf(tmp_str, "%.1f C", temp_c);
	print_string_right(WIDTH-4, 35, tmp_str, FONT_SIZE_MEDIUM);
	
	print_string(4, 50, "Mem", FONT_SIZE_MEDIUM);
	sprintf(tmp_str, "%.0f %%", ram_usage);
	print_string_right(WIDTH-4, 50, tmp_str, FONT_SIZE_MEDIUM);
	
	update_display();
}



// displays the current time in a moving display to reduce OLED burn-in
void show_screensaver() {

    time_t rawtime;
    struct tm *timeinfo;
	char tmp_str[80];
	
	static int top = 0;
	static int centre = 46;

    // Get the current calendar time
    time(&rawtime);

    // Convert to local time
    timeinfo = localtime(&rawtime);

	// move the screen position every minute to reduce screen burn-in
	if ((timeinfo->tm_sec) == 0) {
		top = rand() % 39;
		centre = 46 + (rand() % 30);
	}

	clear_buffer();	
    strftime(tmp_str, sizeof(tmp_str), "%H", timeinfo);	
	print_string_right(centre-6, top, tmp_str, FONT_SIZE_GIANT);
	
	if ((timeinfo->tm_sec % 2) == 1) {
		// Flashing colon
		print_string(centre, top, ".", FONT_SIZE_GIANT);	
	}
	
    strftime(tmp_str, sizeof(tmp_str), "%M", timeinfo);	
	print_string(centre+12, top, tmp_str, FONT_SIZE_GIANT);	

	update_display();
}


