//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
// Get the amount of ram used in percent
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
//------------------------------------------------------------------------------------------------
float get_ram_usage(void) {
	
	long mem_total = -1;
	long mem_free = -1;
	
    FILE *meminfo = fopen("/proc/meminfo", "r");
    if (meminfo == NULL) {
        perror("Failed to open /proc/meminfo");
        exit(1);
    }

    char line[256];

    while (fgets(line, sizeof(line), meminfo)) {
        if (strncmp(line, "MemTotal:", 8) == 0) {
            // Extract the number after "MemTotal:"
            sscanf(line, "MemTotal: %ld kB", &mem_total);

			if ((mem_total >= 0) && (mem_free >= 0))
				break;
        }

        if (strncmp(line, "MemFree:", 8) == 0) {
            // Extract the number after "MemFree:"
            sscanf(line, "MemFree: %ld kB", &mem_free);
			
			if ((mem_total >= 0) && (mem_free >= 0))
				break;
        }	
    }

    fclose(meminfo);
	
	return (1-(float)mem_free/(float)mem_total)*100.0;
}


