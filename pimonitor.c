void handle_signal(int sig) {
	exit_program = 1;
}


int main(void) {

	int idle_counter = 0;
	time_t previous_time = 0;
	exit_program = 0;
	
	// set up Signal handlers to tidy up and turn off the display on CTRL+C etc.
	signal(SIGTERM, &handle_signal);
	signal(SIGINT, &handle_signal);
	signal(SIGQUIT, &handle_signal);
	signal(SIGPWR, &handle_signal);
	
	// seed the random number generator
	srand(time(NULL));
	
    // Initialize display
    ssd1306_init();
	
	// Show the welcome screen for a few seconds to display 
	// the machine name, lan interface name and IP address
	time_t end_time = time(NULL) + SPLASH_SCREEN_TIME;
	show_splashscreen();
	while ((time(NULL) < end_time) && !exit_program)
	{
		nanosleep_ms(100);
	}


	// Show the status screen or screensaver until shutdown
	while (!exit_program) {
		
		time_t t = time(NULL);  // get the seconds since epoch
		
		if (t != previous_time) {

			// Get CPU usage
			CpuStats curr_stats = get_cpu_stats();
			float cpu_usage = calculate_cpu_usage(prev_stats, curr_stats);
			prev_stats = curr_stats; // Update previous stats for next iteration

			float temp_c = get_rpi_temperature();
			float ram_usage = get_ram_usage();		

			// if the processor is busy, stop the screen saver
			if ((cpu_usage >= IDLE_CPU_PERCENT) || 
				(temp_c >= IDLE_TEMP) || 
				(ram_usage >= IDLE_RAM_PERCENT)) {
				idle_counter = 0;
			}
				
			if (idle_counter >= IDLE_TIMER) {
				set_contrast_low();  // reduce brightness and show the screensaver
				show_screensaver();
			}
			else {
				set_contrast_high();  // full brightness.
				show_status(cpu_usage, ram_usage, temp_c);
				idle_counter++;
			}
			
			previous_time = t;
		}
		
		nanosleep_ms(100);
	}

    cleanup();

    return 0;
}
